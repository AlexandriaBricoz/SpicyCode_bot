def get_cases():
    conn = sqlite3.connect('cases.db')
    cursor = conn.cursor()
    cursor.execute('SELECT name, answer, link, photo FROM cases')
    rows = cursor.fetchall()
    conn.close()

    cases = []
    for row in rows:
        name, answer, link, photo = row
        case = Case(name, answer, link, photo)
        cases.append(case)

    return cases

