def init_db():
    conn = sqlite3.connect('cases.db')
    cursor = conn.cursor()
    cursor.execute('''
        CREATE TABLE IF NOT EXISTS cases (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            name TEXT NOT NULL,
            answer TEXT NOT NULL,
            link TEXT,
            photo TEXT
        )
    ''')
    conn.commit()
    conn.close()


